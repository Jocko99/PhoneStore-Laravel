    }

    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        try{
            $this->specificationModel->deleteInternal($id);
            return redirect()->route("internal.index")->with("success","Interna memorija je uspešno obrisana");
        }catch (\Exception $ex){
            return redirect()->route("internal.index")->with("error","Interna memorija se ne može obrisati jer postoje uređaji koji ga koriste");
        }
    }
