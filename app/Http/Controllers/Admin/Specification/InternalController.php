    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(InternalRequest $request)
    {
        $memory = $request->input("internalName");
        try{
            $this->specificationModel->insertInternal($memory);
            return redirect()->route("specification")->with("success","Interna memorija je uspešno dodata.");
        }catch (\Exception $ex){
            return redirect()->route("specification")->with("error",$ex->getMessage());
        }
    }

